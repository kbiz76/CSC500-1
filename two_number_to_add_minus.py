#!/usr/bin/env python3

num1 = float(input('What is your first number? '))
print('Number One: ' + str(num1))

num2 = float(input('What is your second number? '))
print('Number Two: ' + str(num2))

addnum = float(num1 + num2)
minusnum = float(num1 - num2)
print(str(num1) + " + " + str(num2) + " = " + str(addnum))
print(str(num1) + " - " + str(num2) + " = " + str(minusnum))
